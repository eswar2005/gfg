class Solution
{
  public:
    int findOnce(int arr[], int n)
    {
        //code here.
        unordered_map<int ,int > mapp;
        for(int i=0;i<n;i++){
            mapp[arr[i]]++;
        }
        for(auto pair : mapp){
            if(pair.second==1){
                return pair.first;
            }
        }
    }
};


class Solution {
  public:
    vector<int> minAnd2ndMin(vector<int> &arr) {
        // code here
        sort(arr.begin(),arr.end());
        auto it = unique(arr.begin(),arr.end());
        arr.erase(it,arr.end());
        if(arr.size()==1){
            return {-1};
        }
        return {arr[0],arr[1]};
    }
};

